<h1>Busy and Away</h1>

<p class="section_header">About</p>
<div class="section">
	<p>Restores your DND message when you return from AFK.</p>
</div>

<p class="section_header">Commands</p>
<div class="section">
	<ul class="less_indent">
		<li>"/baa" for help.</li>
		<li>"/baa enable" to enable setting your AFK message to your DND message (default). Your DND message will still be restored when returning.</li>
		<li>"/baa disable" to disable setting your AFK message to your DND message.</li>
		<li>"/baa clear" to manually clear your DND for whatever reason.</li>
	</ul>
</div>

