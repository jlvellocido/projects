<h1>Master Looter Assistant</h1>

<p class="section_header">About</p>
<div class="section">
	<p>Automatically sets the loot method to "Master Looter" and threshold to "Epic" (by default but this is customizable) when engaging a raid boss that drops a mount. Once the boss is killed and looted, the settings you previously had before engaging the boss will be restored.</p>
	<p>You need to be in an instance and the leader to use this addon.</p>
</div>

<p class="section_header">Choose your Loot Master!</p>
<div class="section">
	<p>You can opt to set someone else as loot master instead of yourself. Target the desired player and use the command "/mla looter" or from the interface options click the button "Set Target as Loot Master". Use "/mla clear" or the button "Clear Loot Master" to change it back to yourself. If the player is not in the raid group it will default to you. Your loot master will be saved until you clear it.</p>
</div>

<p class="section_header">If you change any loot settings before killing a boss ...</p>
<div class="section">
	<ul class="less_indent">
		<li>Changing the loot method to your previous method will restore your previous settings and act as if you've disabled functionality on the boss. If you wish to reset the pull instead use "/mla reset". This will not actually disable functionality, but rather override it temporarily. To disable, uncheck the boss in the options panel.</li>
		<li>Changing the loot method to a new setting will reset the pull. Your settings will be restored and next time you pull the boss the new settings will be saved to revert to when looting.</li>
	</ul>
</div>

